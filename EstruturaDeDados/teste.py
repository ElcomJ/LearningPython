i = int(input("Digite um valor: "))
j = int(input("Digite outro valor: "))

soma            = i + j
divisao         = i / j
multiplicação   = i * j
subtracao       = i - j

print('\n')

print("A soma de {} + {} é de {}".format(i, j, soma))
print("A divisao de {} e {} é de {}".format(i, j, divisao))
print('A multiplicação de {} e {} é de {}'.format(i, j, multiplicação))
print('A subtracao de {} e {} é de {}'.format(i, j, subtracao))
